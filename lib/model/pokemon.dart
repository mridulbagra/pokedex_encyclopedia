class Pokemon{
  var id;
  String nome;
  var attack;
  var speed;
  var life;
  var defense;
  String element;
  String imageUrl;

  Pokemon(var id, String nome, var attack, var speed, var life, var defense, String element, String imageUrl):
    this.id =id,
    this.nome = nome,
    this.attack = attack,
    this.speed = speed,
    this.life = life,
    this.defense = defense,
    this.element = element,
    this.imageUrl = imageUrl;
}



